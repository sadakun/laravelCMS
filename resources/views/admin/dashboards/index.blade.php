<script>
    $(document).ready(function () {
        //Toggle fullscreen
        $("#cards-fullscreen").click(function (e) {
            e.preventDefault();
            
            var $this = $(this);
        
            if ($this.children('i').hasClass('fas fa-compress'))
            {
                $this.children('i').removeClass('fas fa-compress');
                $this.children('i').addClass('fas fa-expand');
            }
            else if ($this.children('i').hasClass('fas fa-expand'))
            {
                $this.children('i').removeClass('fas fa-expand');
                $this.children('i').addClass('fas fa-compress');
            }
            $(this).closest('.card').toggleClass('cards-fullscreen');
        });
    });
</script>

